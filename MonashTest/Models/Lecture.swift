import Foundation

/// Model defining Lecture
struct Lecture {
    
    /// Lecture Name
    var lectureName: String
    
    /// Lecture Taken By
    var lectureBy: String
    
    /// Address where lecture is taken
    var lectureAddress: String
    
    /// Lecture start time
    var lectureStartTime: Date
    
    /// Lecture end time
    var lectureEndTime: Date                   
}
